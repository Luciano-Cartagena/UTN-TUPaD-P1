#3) Crear una lista vacía, agregar tres palabras con append e imprimir la lista resultante por 
#pantalla. Pista: para crear una lista vacía debes colocar los corchetes sin nada en su interior. Por 
#ejemplo: lista_vacia = []

# Creamos una lista vacía (sin elementos)
lista_vacia = []

# Agregamos la palabra "Hola" a la lista
lista_vacia.append("Hola")

# Agregamos la palabra "Mundo" a la lista
lista_vacia.append("Mundo")

# Agregamos la palabra "Maquina" a la lista
lista_vacia.append("Maquina")

# Imprimimos la lista completa por pantalla
print(lista_vacia)
